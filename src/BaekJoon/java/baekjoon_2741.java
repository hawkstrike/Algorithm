package BaekJoon.java;
// https://www.acmicpc.net/problem/2741

import java.util.Scanner;

public class baekjoon_2741 {
	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		int n = sc.nextInt();

		for(int i = 1; i <= n; i++)
			System.out.println(i);

		sc.close();
	}
}